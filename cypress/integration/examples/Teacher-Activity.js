/// <reference types="Cypress" />
import Activity from '../../support/pageObjects/Activity'
const activity = new Activity()
const Constvarbl = require('../../fixtures/ConstVarbl.json')
describe('My First Test Suite', function() 
{
    it('Signin', ()=>{
        cy.Signin(Constvarbl.TeacherUserName,
            Constvarbl.TeacherPassword);
     });
    it('Create Activity',function() {
        cy.Curriculum()
        activity.getCreateActivity()
            .click({force: true})
        activity.getName()
            .type(Constvarbl.name)
        activity.getClass()
            .contains(Constvarbl.Class)
        activity.getSubject()
            .contains(Constvarbl.Subject)
        activity.getChapter()
            .contains(Constvarbl.Chapter)
        cy.wait(2000)
        activity.getSubjectTopics()
            .contains(Constvarbl.Topic)
                .click({force: true})    
        activity.getSubjectTopics()
            .contains(Constvarbl.SubTopic)
                .click({force: true})
        activity.getDescription()
            .type(Constvarbl.Description)
        activity.getCreateActivtyPush()
            .click({force: true})
        activity.getCheckbox()
            .check()
                .should('be.checked')
        //cy.get('[ng-click="cancelPush()"]').click()
        activity.getStartCalender()
            .click()
        activity.getDateandTime()
            .contains(Constvarbl.pushDate)
                .click()
        activity.getCalenderHour()
            .contains(Constvarbl.pushHour1)
                .click()
        activity.getCalenderMinute()
            .contains(Constvarbl.pushMin1)
                .click()
        activity.getEndCalender()
            .click()
        activity.getDateandTime1()
            .contains(Constvarbl.pushDate)
                .click()
        activity.getCalenderHour1()
            .contains(Constvarbl.pushHour2)
                .click()
        activity.getCalenderMinute1()
            .contains(Constvarbl.pushMin2)
                .click()
        activity.getCancel()
            .click()
    })
    it('View Activity',function() {    
        activity.getViewActivity()
            .click({force: true})
        activity.getClass()
            .contains(Constvarbl.Class)
        activity.getSubject()
            .contains(Constvarbl.Subject)
        activity.getChapter()
            .contains(Constvarbl.Chapter)
        activity.getSelectDate()
            .contains(Constvarbl.CreatedDate)
        activity.getCalender1()
            .click()
        activity.getDate()
            .contains(Constvarbl.Date1)
                .click()
        activity.getCalender2()
            .click()
        activity.getDate()
            .contains(Constvarbl.Date2)
                .click()
        activity.getPushedButton()
            .click()
        activity.getActivityPushedTable().each(($e1,index,$list)=>{
            const text = $e1.text()
            if(text.includes("26-8-20 3 mk test"))
            {
                $e1.click()
            }
        })
        activity.getActivityEyeview()
            .click()
        activity.getFullScreen()
            .click()
        activity.getVideoMute()
            .click()
        activity.getvideoPlay()
            .click()
        activity.getActivityClose()
            .click({force: true})
        activity.getBack()
            .click()
        cy.Logout()
    })
})    