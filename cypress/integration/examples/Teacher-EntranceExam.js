/// <reference types="Cypress" />
import EntranceExam from '../../support/pageObjects/EntanceExm'
const entrance = new EntranceExam()
const entrnce = require('../../fixtures/Entrance.json')
describe('My First Test Suite', function() 
{
    it('Signin', ()=>{
        cy.Signin(entrnce.TeacherUserName,
            entrnce.TeacherPassword);
     }); 
    it('NEET Exam',function() {
        cy.Curriculum()
        entrance.getNEETexam()
            .click({force:true})
        entrance.getSubChemistry()
            .click()    
        entrance.getSubPhysics()
            .click()
        entrance.getExamChapter()
            .contains(entrnce.EntrancChpter)
        entrance.getExamConcepts()
            .click()
        cy.contains(entrnce.RelatedChapter)
            .click()
        cy.contains('>')
            .click({force: true})// next pdf page
        cy.contains('+')
            .click({force: true})
        cy.contains(entrnce.FitPage)
            .click()
        entrance.getNavigateback()
            .click()
    })
    it('EAMCET',function() {
        entrance.getEAMCETexam()
            .click({force:true})
        entrance.getSubMathematics1()
            .click()
        entrance.getSubChemistry1()
            .click()    
        entrance.getSubPhysics1()
            .click()
        entrance.getExamChapter2()
            .contains(entrnce.EntrancChpter1)
        entrance.getExamConcepts1()
            .click({force:true})
        cy.contains(entrnce.RelatedChapter1)
            .click()
        cy.contains('>')
            .click({force: true})// next pdf page
        cy.contains('+')
            .click({force: true})
        cy.contains(entrnce.FitPage)
            .click()
        entrance.getNavigateback()
            .click()
    })
    it('IIT-JEE Mains',function() {
        entrance.getIITexam()
            .click({force:true})
        entrance.getSubMathematics()
            .click()
        entrance.getSubChemistry()
            .click()    
        entrance.getSubPhysics()
            .click()
        entrance.getExamChapter1()
            .contains(entrnce.EntrancChpter)
        entrance.getExamConcepts()
            .click()
        cy.contains(entrnce.RelatedChapter)
            .click()
        cy.contains('>')
            .click({force: true})// next pdf page
        cy.contains('+')
            .click({force: true})
        cy.contains(entrnce.FitPage)
            .click()
        entrance.getNavigateback()
            .click()
        //cy.Logout()
    })
})    